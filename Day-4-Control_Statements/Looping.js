//Loop statements are used to do repeated tasks
//for loop (initialization,condition,incrementdecrement)

for(let i =0;i<=10;i++){
    console.log(i);
}

//while loop first initialize variable provide condition and do increment or decrement

let a = 1;
while(a<=7){
    console.log(a);
    a++;
}

//do while - same as while except it runs one time before checking the condition

let num = 0;
do{
    console.log(num);
    num++;
}while(num<=10);