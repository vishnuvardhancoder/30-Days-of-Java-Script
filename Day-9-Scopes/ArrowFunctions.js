//Normal Function

let sum = function(num1,num2){
    return num1+num2
}
console.log(sum(3,4));

//Arrow Function

let add =(num1,num2) => num1+num2; //when you want to return only one statement you need not to put {} and a return.
console.log(add(3,4))

//Arrow functions works same as normal function but it has shorter syntax it is introduced in ES6